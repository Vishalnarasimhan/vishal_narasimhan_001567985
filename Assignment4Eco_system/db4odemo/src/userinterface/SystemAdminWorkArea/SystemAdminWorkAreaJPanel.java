    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        String type = jComboBox1.getSelectedItem().toString();
            if(activePanel!=null) remove(activePanel);
        revalidate();
        repaint();
        if(type.equals("Managers")){
            activePanel = new Create_RestaurantManagerJPanel();
        }
        else if(type.equals("Restaurants")){
            activePanel = new CreateRestaurant();
        }
        else if(type.equals("Customers")){
            activePanel = new Create_CustomerJPanel();
        }
        else if(type.equals("Delivery Men")){
            activePanel = new Create_DeliveryMenJPanel();
        }
        activePanel.setBounds(200, 10, 273, 196);
        activePanel.setSize(400, 300);
        this.add(activePanel);
        revalidate();
    }//GEN-LAST:event_createButtonActionPerformed

